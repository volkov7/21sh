#include "libft.h"

int		ft_atoi(const char *str)
{
	long long	k;
	long long	j;
	int			f;

	f = 1;
	k = 0;
	while ((*str == 32) || ((*str >= 9) && (*str <= 13)))
		++str;
	if (*str == '-')
		f = -1;
	if (*str == '-' || *str == '+')
		++str;
	while (*str >= '0' && *str <= '9')
	{
		j = k;
		k = k * 10 + (*(str++) - '0');
		if (j > k)
			return (f > 0) ? -1 : 0;
	}
	return (f * (int)k);
}
