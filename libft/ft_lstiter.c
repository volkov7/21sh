#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *tp;

	if (lst && f)
		while (lst)
		{
			tp = lst->next;
			f(lst);
			lst = tp;
		}
}
