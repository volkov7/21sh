#include "ft_printf.h"

long double	ft_pow(long long nbr, int n)
{
	long double x;

	x = 1;
	if (n == 0)
		return (1);
	if (n == 1)
		return (nbr);
	while (n-- > 0)
		x *= nbr;
	return (x);
}
