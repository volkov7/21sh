#include "libft.h"

void	ft_putnbr(int nb)
{
	char	s[12];
	int		i;

	i = 0;
	if (nb == 0)
		ft_putchar('0');
	else if (nb == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (nb < 0)
		{
			ft_putchar('-');
			nb *= -1;
		}
		while (nb)
		{
			s[i++] = '0' + nb % 10;
			nb /= 10;
		}
	}
	while (i)
		ft_putchar(s[--i]);
}
