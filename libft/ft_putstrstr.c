#include "libft.h"

void	ft_putstrstr(const char **str, size_t len)
{
	if (str)
	{
		while (len > 0)
		{
			while (*str)
			{
				ft_putstr(*(str++));
				ft_putchar('\n');
			}
			len--;
		}
	}
}
