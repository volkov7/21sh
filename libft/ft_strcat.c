#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int j;
	int len;

	i = 0;
	j = 0;
	while (s1[i])
		++i;
	len = ft_strlen((char*)s2);
	while (j < len)
	{
		s1[i + j] = s2[j];
		++j;
	}
	s1[i + j] = '\0';
	return (s1);
}
